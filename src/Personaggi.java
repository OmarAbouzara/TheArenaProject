public class Personaggi {

    String nome;
    int potenzamin;
    int vita;
    int velocità;
    int potenzamax;

    public Personaggi(String nome, int potenzamin, int vita, int velocità, int potenzamax) {
        this.potenzamax = potenzamax;
        this.potenzamin = potenzamin;
        this.nome = nome;
        this.vita = vita;
        this.velocità = velocità;
    }
}

